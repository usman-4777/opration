     *
     * @param  \App\Models\Expense  $expense
     * @return \Illuminate\Http\Response
     */
    public function destroy(Expense $expense)
    {
        $expense->delete();
        return response()->json([
            'status' => 'success',
            'message' => 'expens deleted successfully',
            'data' => $expense
        ],200);
